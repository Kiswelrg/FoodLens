"""Build a *citable* evaluation set: natural-language queries an LLM writes for
held-out recipes, deliberately NOT copying the document text.

This replaces the old bootstrap pseudo-queries (first 15 words of a recipe's own
description), which leaked the answer into the query and inflated absolute scores.
Here each query is generated by DeepSeek from a recipe it can see, but phrased as
a hungry user who has *not* seen that recipe — so the gold document is a genuine
known-item retrieval target, not a copy of the query.

Each eval item:
    query            - the user-style search string (scored against the index)
    gold_id          - the source recipe's id (the single known-item gold doc)
    gold_name        - for eyeballing
    gold_text        - the recipe card (RAGAS reference context for recall)
    reference        - one-sentence ground-truth answer (RAGAS reference for precision)

We let queries name concrete ingredients / a diet flag / a time or calorie target
(real users do — and that exact-token regime is where sparse + hybrid earn their
lift), but we reject any query that copies the recipe name or a 5-gram from its
description. The leak check is reported so the benchmark can claim it.
"""
import json
import random
import re

import config
from llm import get_deepseek

EVAL_SET_PATH = config.INDEX_DIR / "eval_set.json"

SYSTEM = (
    "You write realistic recipe-search queries for evaluating a retrieval system. "
    "Given a recipe, imagine a user who WANTS a dish like this but has never seen "
    "this particular recipe, and write the short query they'd type."
)

INSTRUCTIONS = """\
For each numbered recipe below, write ONE search query and ONE reference answer.

Rules for the QUERY (this is the hard part):
- Phrase it as a natural user request, 5-18 words. Mix intent with specifics.
- You MAY name 1-3 concrete ingredients, a dietary constraint (e.g. vegetarian,
  gluten-free), or a time/calorie target — real users include these.
- Do NOT copy the recipe's name. Do NOT copy any phrase from its description.
  Re-describe the dish in your own words from the user's point of view.

Rules for the REFERENCE (this is the ideal answer, not the query):
- One sentence naming the dish type and its key ingredients. It MAY use specifics
  freely; it represents ground truth.

Return a JSON object with this exact shape:
{"items": [{"i": <recipe number>, "query": "<string>", "reference": "<string>"}, ...]}
Include one object per recipe, using the recipe's number as "i".
"""

_WORD = re.compile(r"\w+")


def _recipe_card(doc: dict, max_chars: int = 700) -> str:
    """Compact view shown to the generator (full text would waste tokens)."""
    return doc["text"][:max_chars]


def _ngrams(text: str, n: int) -> set:
    toks = _WORD.findall(text.lower())
    return {tuple(toks[i:i + n]) for i in range(len(toks) - n + 1)}


def leak_check(query: str, doc: dict, n: int = 5) -> bool:
    """True if the query leaks the source: copies the recipe name verbatim, or
    shares an n-gram with the recipe's description."""
    q = query.lower()
    if doc["name"].lower() in q:
        return True
    desc = (doc["metadata"].get("description") or "")
    return bool(_ngrams(query, n) & _ngrams(desc, n))


def _generate_batch(llm, batch: list[dict]) -> dict[int, dict]:
    cards = "\n\n".join(f"[{i}] {_recipe_card(d)}" for i, d in enumerate(batch))
    prompt = f"{INSTRUCTIONS}\n\nRecipes:\n{cards}"
    raw = llm.invoke([("system", SYSTEM), ("human", prompt)]).content
    data = json.loads(raw)
    out = {}
    for item in data.get("items", []):
        try:
            out[int(item["i"])] = {"query": item["query"].strip(),
                                   "reference": item["reference"].strip()}
        except (KeyError, ValueError, AttributeError):
            continue
    return out


def build(n: int = 150, seed: int = 0, batch_size: int = 8) -> list[dict]:
    """Sample n recipes from the docstore and generate non-leaking queries."""
    with open(config.INDEX_DIR / "docstore.json") as f:
        docs = json.load(f)["docs"]
    pool = [d for d in docs if (d["metadata"].get("description") or "").strip()]
    random.seed(seed)
    sample = random.sample(pool, min(n, len(pool)))

    llm = get_deepseek(temperature=0.7, max_tokens=4096, json_mode=True)
    items, leaks = [], 0
    for start in range(0, len(sample), batch_size):
        batch = sample[start:start + batch_size]
        try:
            gen = _generate_batch(llm, batch)
        except (json.JSONDecodeError, Exception) as e:  # noqa: BLE001
            print(f"  batch {start//batch_size} failed ({e}); skipping")
            continue
        for i, d in enumerate(batch):
            g = gen.get(i)
            if not g or not g["query"]:
                continue
            if leak_check(g["query"], d):
                leaks += 1
                continue  # drop leaked queries rather than keep cheating ones
            items.append({
                "query": g["query"],
                "gold_id": d["id"],
                "gold_name": d["name"],
                "gold_text": d["text"],
                "reference": g["reference"],
            })
        print(f"  generated {len(items)} kept / {leaks} leaked so far")

    EVAL_SET_PATH.write_text(json.dumps(items, indent=2))
    print(f"\nSaved {len(items)} eval queries to {EVAL_SET_PATH} "
          f"({leaks} dropped for leaking the source doc)")
    return items


def load() -> list[dict]:
    if not EVAL_SET_PATH.exists():
        raise FileNotFoundError(
            f"{EVAL_SET_PATH} not found. Run `python eval_set.py` first.")
    return json.loads(EVAL_SET_PATH.read_text())


if __name__ == "__main__":
    build()
